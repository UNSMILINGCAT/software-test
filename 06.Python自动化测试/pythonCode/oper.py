# 算术运算符  +  -  *  /
print(19+29)
print(19-29)
print(19*29)
print(29/19)

# 算术运算符  // 取整   舍去小数
print(29//19)

# 算术运算符  % 取余
print(19%29)
print(5%2)

# 算术运算符  ** 乘方
print(10**2)

# 优先级关系：** 大于 *，/，//，% 大于 +，-


