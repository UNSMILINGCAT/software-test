# 变量的声明
# 变量的命名规范
#1.变量名必须由字母，数字，下划线组成，且不能是数字开头
a111=100;# 对
_abc=100;#对
abc_123=200;# 对
#123=123;错
#abc@#$ = 123;错


#2.不可以使用关键字作为变量名
#常见的关键字  input  if  while for ....
#input=123;错

#3.变量名要见名知意
#4.最好使用驼峰式命名
# 如果是一个单词的变量名，全部字母小写，如果是多个单词组成的变量名，
# 则第一个单词全部小写，后面的单词首字母大写
userName = "abc";

print(int(10101))

# 字符串拼接运算符  +
print("aa"+"bb")
